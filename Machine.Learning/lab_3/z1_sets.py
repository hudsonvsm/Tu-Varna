# Разгледайте  самостоятелно  методите  на  множество,  като използвате help(set). Тествайте със собствени примери.

A = {1, 2, 3, 'b', 'a'}

B = {2, 1, 4, 'a'}

C = {'a', 'b'}

print(A.union(B))

print(A.intersection(B))

print(A.symmetric_difference(B))

print(A.difference(B))

print(A.issubset(B))

print(A.issuperset(C))


